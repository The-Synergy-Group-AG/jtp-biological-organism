def get_context_for_operation(operation_type: str) -> Dict[str, Any]:
    """Get operational context configuration for a specific biological operation"""
    if operation_type in BIOLOGICAL_REQUEST_CONTEXTS:
        return BIOLOGICAL_REQUEST_CONTEXTS[operation_type]

    return BIOLOGICAL_REQUEST_CONTEXTS["consciousness_harmony_orchestration"]  # Default context

def get_processing_constants_for_operation(operation_type: str, subsystem_name: str = None) -> Dict[str, Any]:
    """Get processing constants for a specific operation and/or subsystem"""

    # Get base constants
    constants = PROCESSING_CONSTANTS.copy()

    # Add context-specific overrides if available
    context = get_context_for_operation(operation_type)
    if subsystem_name and f"{subsystem_name}_specific" in context:
        constants.update(context[f"{subsystem_name}_specific"])

    return constants

def validate_operation_type(operation_type: str) -> bool:
    """Validate that an operation type is recognized"""
    return operation_type in BIOLOGICAL_OPERATION_TYPES

def get_operation_requirements(operation_type: str) -> List[str]:
    """Get subsystem requirements for a specific operation type"""
    context = get_context_for_operation(operation_type)
    return context.get("processing_requirements", [])

def get_operation_priority(operation_type: str) -> str:
    """Get priority level for a specific operation type"""
    context = get_context_for_operation(operation_type)
    return context.get("priority", "low")

def get_operation_expected_duration(operation_type: str) -> float:
    """Get expected duration for a specific operation type"""
    context = get_context_for_operation(operation_type)
    return context.get("expected_duration", 30.0)

